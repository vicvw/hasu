module Objects.Character
    ( char
    , default_8_16
    ) where


import General

import Data.Maybe               (fromJust)
import qualified Data.Map as M  (fromList)


instance Drawable Character where
    points (Character strings value (Coord x0 y0) (Coord w h))
        = M.fromList
        . map (\(a, _) -> (a, value))
        . concatMap (filter ((/= ' ') . snd))
        . map (uncurry zip)
        . (`zip` strings)
        $ [ zipWith Coord xs y | y <- ys ]

        where
        xs = [x0..x0 + w]
        ys = map repeat [y0 + h, y0 + h - 1..y0]


char :: String -> (Integer, Integer) -> Font -> Value -> Character
char string orig font val = Character
    (fromJust $ lookup string font)
    val
    (uncurry Coord orig)
    (Coord 8 16)


